#include<iostream>
using namespace std;

int main()
{
    // Variable Declaration

    char grade;
    int age;
    float marks;
    double salary;
   
    // Variable Initialization

    grade = 'A';
    age = 25;
    marks = 99.89f;
    salary = 78.67890;

    // Print Values

    cout << "Grade : " << grade << endl;
    cout << "Age : " << age << endl;
    cout << "Marks : " << marks << endl;
    cout << "Salary : " << salary << endl;

    return 0;
}